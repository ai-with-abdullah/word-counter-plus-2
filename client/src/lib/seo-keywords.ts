// Get all keywords for a tool
export function getToolKeywords(toolId: string): ToolKeywords | null {
  const keywords = SEO_KEYWORDS[toolId as keyof typeof SEO_KEYWORDS];
  return keywords || null;
}

// Get all keywords as a comma-separated string
export function getKeywordsString(toolId: string): string {
  const keywords = getToolKeywords(toolId);
  if (!keywords) return '';
  
  return [
    ...keywords.primary,
    ...keywords.lowCompetition.slice(0, 15), // Limit low competition for meta
    ...keywords.highCompetition
  ].join(', ');
}

// Get hashtags for a specific platform
export function getPlatformHashtags(platform: keyof typeof PLATFORM_HASHTAGS): string[] {
  return PLATFORM_HASHTAGS[platform] || [];
}

// Get all hashtags for a tool
export function getToolHashtags(toolId: string): string[] {
  const keywords = getToolKeywords(toolId);
  return keywords?.hashtags || [];
}

// Get combined hashtags (tool + platform)
export function getCombinedHashtags(
  toolId: string, 
  platform: keyof typeof PLATFORM_HASHTAGS
): string {
  const toolHashtags = getToolHashtags(toolId).slice(0, 5);
  const platformHashtags = getPlatformHashtags(platform).slice(0, 5);
  return [...toolHashtags, ...platformHashtags].join(' ');
}
